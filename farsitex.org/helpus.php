<p>
  Although FarsiTeX may be distributed freely, the production and
  maintenance of the system does require expenditure of reasonably large
  sums of money.  There are many necessities that need substantial
  financing: examples are new or enhanced computing equipment.
</p>
  
<p>
  This is why we are appealing to you for contributions to the fund.
  Any sum will be much appreciated; the amount need not be large as
  small contributions add up to very useful amounts.  Contributions of
  suitable equipment and software will also be of great value.  This
  appeal is both to you as an individual author and to you as a member
  of a group or as an employee: please encourage your department or your
  employer to contribute towards sustaining our work.
</p>
<p>

  To donate to FarsiTeX, click <a href="http://sourceforge.net/donate/index.php?group_id=7710">here</a>.
  For non-monetary contributions please contact
  Behdad Esfahbod (farsitex __at__ behdad __dot__ org).
</p>

<p>
  During this years of free software development for the sake
  of public, many have supported or helped the project.
  FarsiTeX has been mainly supported by the long-time contributions of:
  <ul>
    <li>
      Vice Minister on Education,
      Ministry of Culture and Higher Education
    </li>
    <li>
      Vice Chancellor on Research,
      Sharif University of Technology
    </li>
    <li>
      Iran High Council of Informatics
    </li>
    <li>
      Data Processing Deputy, Statistical Center of Iran
    </li>
    <li>
      Computing Center, Sharif University of Technology
    </li>
  </ul>
  as well as donations by various other individual and entities,
  including but not limited to:
  <div class="donors">
